def drawPixel(t,c,x,y):
    t.pu()
    t.goto(x,y)
    t.fillcolor(c)
    t.pd()
    t.begin_fill()
    for i in range(4):
        t.fd(20)
        t.right(90)
    t.end_fill()

x = -400
y = 400

def drawGrid():
    x = -400
    y = 400
    for i in range(40):
        x = -400
        for i in range(40):
            drawPixel(joe,"white",x,y)
            x = x + 20
        y = y - 20
        joe.pu()
        joe.goto(-400,y)
        joe.pd()

